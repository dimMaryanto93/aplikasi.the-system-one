	private void dataDetail(int baris){
		this.txtUsername.setText(getTabelModel().getValueAt(baris, 0).toString());
		this.txtPassword.setText(getTabelModel().getValueAt(baris, 1).toString());
		this.txtConfPass.setText(getTabelModel().getValueAt(baris, 1).toString());
		this.cboHak.setSelectedIndex(Integer.valueOf(getTabelModel().getValueAt(baris, 2).toString()));
	}
	
	private int getJumlahDataTabel(){
		return getTabelModel().getRowCount()-1;
	}
	
