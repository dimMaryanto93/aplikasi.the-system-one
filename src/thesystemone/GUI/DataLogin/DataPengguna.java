        private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
                // TODO add your handling code here:
		String us = txtUsername.getText();		
		int hak = cboHak.getSelectedIndex();
		if(isSimpanTambah()){
			opPengguna.simpanData(us, getPass2(), hak);
			tampilToTabel(PerintahSQL.getPerintahSelect(PerintahSQL.tabelLogin));
			dataDetail(getJumlahDataTabel() -1);
		}else{
			opPengguna.ubahData(us, getPass2(), hak);
			simpanDataTambah = true;
			tampilToTabel(PerintahSQL.getPerintahSelect(PerintahSQL.tabelLogin));
			dataDetail(0);
		}
		aturKomponen(true, false, "Delete");
        }//GEN-LAST:event_btnSaveActionPerformed

        private void txtConfPassCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txtConfPassCaretUpdate
                // TODO add your handling code here:
		if(getPass1().equals("")||getPass2().equals("")){
			btnSave.setEnabled(false);
		}else if(getPass2().equals(getPass1())||getPass1().equals(getPass2())){
			btnSave.setEnabled(true);
		}else{
			btnSave.setEnabled(false);
		}
        }//GEN-LAST:event_txtConfPassCaretUpdate

